package ru.academits.jfb2018.shelestov.lesson2;

public class TextLines {
    public static void main(String[] args) {
        System.out.println("Я узнал, что у меня" + System.lineSeparator() +
                "Есть огромная семья" + System.lineSeparator() +
                "И тропинка и лесок" + System.lineSeparator() +
                "В поле каждый колосок" + System.lineSeparator());

        System.out.println("\"30\" августа 2014г.");
        System.out.println("C:\\Windows\\System32\\Drivers\\etc\\host" + System.lineSeparator());

        int result = 7 * 7;
        System.out.println("Результат = " + result);
    }
}
