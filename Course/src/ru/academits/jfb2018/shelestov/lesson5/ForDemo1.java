package ru.academits.jfb2018.shelestov.lesson5;

public class ForDemo1 {
    public static void main(String[] args) {
        for (int i = 0; i <= 100; i++) {
            System.out.print(i + " ");
        }

        System.out.println();

        for (int i = 7; i <= 121; i++) {
            System.out.print(i + " ");
        }

        System.out.println();

        for (int i = 100; i >= 30; i--) {
            System.out.print(i + " ");
        }
    }
}
